package org.etiya;

public class Calculation {

    public int total(int number1,int number2){
        return number1+number2;
    };
    public int total(int number1,int number2,int number3){
        return number1+number2+number3;
    };


  /*
   --- Overloading
           (aşırı yükleme veya aşırı yüklenme olarak da bilinir), aynı isimde
           fakat farklı parametre listelerine sahip birden fazla metodu
           bir sınıf içinde tanımlama yeteneğidir.
  */

}
